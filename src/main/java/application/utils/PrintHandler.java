package application.utils;

/**
 * The PrintHandler class provides a utility method for printing strings to the console.
 */
public class PrintHandler {

  /**
   * Constructs a new PrintHandler instance.
   */
  public PrintHandler() {
    // Default constructor
  }

  /**
   * Prints the provided string to the console.
   *
   * @param providedString the string to be printed
   */
  public void printString(String providedString) {
    System.out.println(providedString);
  }
}